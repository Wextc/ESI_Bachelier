# Objectif : calculer l’aire d’un carré à partir de la longueur de son côté.

# Lecture de la longueur du côté
cote = float(input("Entrez la longueur du côté du carré : "))

# Calcul de l’aire
aire = cote ** 2

# Affichage du résultat
print("L’aire du carré est :", aire)

